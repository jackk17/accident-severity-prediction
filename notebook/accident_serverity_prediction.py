# Fonction pour catégoriser les heures
def categoriser_heure(heure):
    if pd.isna(heure):
        return 'Inconnu'  # Pour les valeurs manquantes
    elif 0 <= heure < 8:
        return '00h-08h'
    elif 8 <= heure < 17:
        return '08h-17h'
    elif 17 <= heure < 23:
        return '17h-23h'
    else:
        return '23h-00h'  # Pour les heures entre 23h et 00h

# Appliquer la catégorisation
